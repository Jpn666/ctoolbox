uintxx
base32_encode(const uint8* in, uintxx size, uint8* out)
{
	uint8* tmp;
	ASSERT(in && out);
	
	tmp = (void*) out;
	for (; size >= 5; size -= 5) {
		*out++ = b32_table[((in[0] & 0xF8) >> 3)];
		*out++ = b32_table[((in[1] & 0xC0) >> 6) | ((in[0] & 0x07) << 2)];
		*out++ = b32_table[((in[1] & 0x3E) >> 1)];
		*out++ = b32_table[((in[2] & 0xF0) >> 4) | ((in[1] & 0x01) << 4)];
		*out++ = b32_table[((in[3] & 0x80) >> 7) | ((in[2] & 0x0F) << 1)];
		*out++ = b32_table[((in[3] & 0x7C) >> 2)];
		*out++ = b32_table[((in[4] & 0xE0) >> 5) | ((in[3] & 0x03) << 3)];
		*out++ = b32_table[in[4] & 0x1F];
		in += 5;
	}
	
	switch (size) {
		case 1:
			*out++ = b32_table[((in[0] & 0xF8) >> 3)];
			*out++ = b32_table[((in[0] & 0x07) << 2)];
			*out++ = 0x3D;
			*out++ = 0x3D;
			*out++ = 0x3D;
			*out++ = 0x3D;
			*out++ = 0x3D;
			*out++ = 0x3D;
			break;
		case 2:
			*out++ = b32_table[((in[0] & 0xF8) >> 3)];
			*out++ = b32_table[((in[1] & 0xC0) >> 6) | ((in[0] & 0x07) << 2)];
			*out++ = b32_table[((in[1] & 0x3E) >> 1)];
			*out++ = b32_table[((in[1] & 0x01) << 4)];
			*out++ = 0x3D;
			*out++ = 0x3D;
			*out++ = 0x3D;
			*out++ = 0x3D;
			break;
		case 3:
			*out++ = b32_table[((in[0] & 0xF8) >> 3)];
			*out++ = b32_table[((in[1] & 0xC0) >> 6) | ((in[0] & 0x07) << 2)];
			*out++ = b32_table[((in[1] & 0x3E) >> 1)];
			*out++ = b32_table[((in[2] & 0xF0) >> 4) | ((in[1] & 0x01) << 4)];
			*out++ = b32_table[((in[2] & 0x0F) << 1)];
			*out++ = 0x3D;
			*out++ = 0x3D;
			*out++ = 0x3D;
			break;
		case 4:
			*out++ = b32_table[((in[0] & 0xF8) >> 3)];
			*out++ = b32_table[((in[1] & 0xC0) >> 6) | ((in[0] & 0x07) << 2)];
			*out++ = b32_table[((in[1] & 0x3E) >> 1)];
			*out++ = b32_table[((in[2] & 0xF0) >> 4) | ((in[1] & 0x01) << 4)];
			*out++ = b32_table[((in[3] & 0x80) >> 7) | ((in[2] & 0x0F) << 1)];
			*out++ = b32_table[((in[3] & 0x7C) >> 2)];
			*out++ = b32_table[((in[3] & 0x03) << 3)];
			*out++ = 0x3D;
	}
	return out - tmp;
}


#define ISSPACE(X) \
	(X) == 0x20 || \
	(X) == 0x09 || \
	(X) == 0x0A || \
	(X) == 0x0B || \
	(X) == 0x0C || (X) == 0x0D

#if BASE32_CFG_IGNORE_WHITESPACE
#	define BASE32_FILTER_CHAR(X) ((X) == 0x3D || ISSPACE(X))
#else
#	define BASE32_FILTER_CHAR(X) ((X) == 0x3D)
#endif


uintxx
base32_decode(const uint8* in, uintxx size, uint8* out)
{
	uint8* tmp;
	uint8  b[8];
	uintxx i;
	ASSERT(in && out);
	
	tmp = (void*) out;
	i   = 0;
	while (size) {
		for (i = 0; i < 8 && size; in++) {
			if (BASE32_FILTER_CHAR(in[0])) {
				size--;
				continue;
			}
			/* the rfc4648 recomends to reject the entire encode if a non
			 * alfabet character is found on the stream. */
			if ((b[i++] = b32_chindex[in[0]]) == 0x20)
				return 0;
			size--;
		}
		if (i == 8) {
			*out++ = ((b[0] << 3) | (b[1] >> 2));
			*out++ = ((b[1] << 6) | (b[2] << 1) | (b[3] >> 4));
			*out++ = ((b[3] << 4) | (b[4] >> 1));
			*out++ = ((b[4] << 7) | (b[5] << 2) | (b[6] >> 3));
			*out++ = ((b[6] << 5) | (b[7]));
		}
	}
	
	switch (i) {
		case 2:
			*out++ = ((b[0] << 3) | (b[1] >> 2));
			break;
		case 4:
			*out++ = ((b[0] << 3) | (b[1] >> 2));
			*out++ = ((b[1] << 6) | (b[2] << 1) | (b[3] >> 4));
			break;
		case 5:
			*out++ = ((b[0] << 3) | (b[1] >> 2));
			*out++ = ((b[1] << 6) | (b[2] << 1) | (b[3] >> 4));
			*out++ = ((b[3] << 4) | (b[4] >> 1));
			break;
		case 7:
			*out++ = ((b[0] << 3) | (b[1] >> 2));
			*out++ = ((b[1] << 6) | (b[2] << 1) | (b[3] >> 4));
			*out++ = ((b[3] << 4) | (b[4] >> 1));
			*out++ = ((b[4] << 7) | (b[5] << 2) | (b[6] >> 3));
	}
	return out - tmp;
}

uintxx
base32_getdecodesz(const uint8* in, uintxx size)
{
	uintxx i;
	ASSERT(in);
	
	i = 0;
	while (size--) {
		if (!BASE32_FILTER_CHAR(in[0])) {
			i++;
			if (b32_chindex[in[0]] == 0x20)
				return 0;
		}
		in++;
	}
	return (uintxx) (i * (5.0f / 8.0f));
}

